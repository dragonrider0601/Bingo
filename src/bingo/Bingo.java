    private void callMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_callMousePressed
        if(uncalledNumbers.size()>0){
            int number=uncalledNumbers.get(gen.nextInt(uncalledNumbers.size()));
            if(number>0&&number<=15){
                calledNumber.setText("B-"+String.valueOf(number));
            }
            else if(number>15&&number<=30){
                calledNumber.setText("I-"+String.valueOf(number));
            }
            else if(number>30&&number<=45){
                calledNumber.setText("N-"+String.valueOf(number));
            }
            else if(number>45&&number<=60){
                calledNumber.setText("G-"+String.valueOf(number));
            }
            else{
                calledNumber.setText("O-"+String.valueOf(number));
            }
            calledNumbers.add(number);
            uncalledNumbers.remove(uncalledNumbers.indexOf(number));
            ArrayList<Integer> sortedList=sortList(calledNumbers);
            String bNums="";
            String iNums="";
            String nNums="";
            String gNums="";
            String oNums="";
            for(int num:sortedList){
                if(num>0&&num<=15){
                    bNums+=num+", ";
                }
                else if(num>15&&num<=30){
                    iNums+=num+", ";
                }
                else if(num>30&&num<=45){
                    nNums+=num+", ";
                }
                else if(num>45&&num<=60){
                    gNums+=num+", ";
                }
                else{
                    oNums+=num+", ";
                }
            }
            bCalled.setText(bNums);
            iCalled.setText(iNums);
            nCalled.setText(nNums);
            gCalled.setText(gNums);
            oCalled.setText(oNums);
            numCount++;
            count.setText(String.valueOf(numCount));
        }
    }//GEN-LAST:event_callMousePressed

