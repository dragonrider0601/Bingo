    public ArrayList<Integer> sortList(ArrayList<Integer> unsortedList){
        ArrayList<Integer> input=new ArrayList<Integer>();
        for(int x: unsortedList){
            input.add(x);
        }
        ArrayList<Integer> output=new ArrayList<Integer>();
        for(int numsLeft=0; numsLeft<unsortedList.size(); numsLeft++){
            int min=input.get(0);
            for(int i:input){
                if(i<=min){
                    min=i;
                }
            }
            output.add(min);
            input.remove(input.indexOf(min));
        }
    return output;
    }
    
